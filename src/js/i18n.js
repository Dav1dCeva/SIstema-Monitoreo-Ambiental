export function t(key, params = {}) {
  let str = translations[currentLang]?.[key] || translations['es']?.[key] || key;
  Object.entries(params).forEach(([k, v]) => {
    str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
  });
  return str;
}

export function getLang() {
  return currentLang;
}

export function setLang(lang) {
  currentLang = lang;
  try { localStorage.setItem(LANG_KEY, lang); } catch(e) {}
  applyTranslations();
  document.documentElement.lang = lang;
  window.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

export function toggleLang() {
  setLang(currentLang === 'es' ? 'en' : 'es');
}

export function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = t(key);
    if (text !== key) el.textContent = text;
  });
  document.querySelectorAll('[data-i18n-attr]').forEach(el => {
    el.getAttribute('data-i18n-attr').split(',').forEach(pair => {
      const [attr, key] = pair.split(':');
      if (attr && key) {
        const val = t(key);
        if (val !== key) el.setAttribute(attr.trim(), val);
      }
    });
  });
}

export function buildLangToggle() {
  const btn = document.createElement('button');
  btn.id = 'lang-toggle';
  btn.className = 'lang-toggle-btn';
  btn.textContent = t('lang.toggle');
  btn.setAttribute('aria-label', currentLang === 'es' ? 'Switch to English' : 'Cambiar a español');
  btn.setAttribute('data-tooltip', currentLang === 'es' ? 'Switch to English' : 'Cambiar a español');
  btn.addEventListener('click', () => {
    toggleLang();
    btn.textContent = t('lang.toggle');
    btn.setAttribute('aria-label', currentLang === 'es' ? 'Switch to English' : 'Cambiar a español');
    btn.setAttribute('data-tooltip', currentLang === 'es' ? 'Switch to English' : 'Cambiar a español');
  });
  return btn;
}

